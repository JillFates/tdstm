'use strict';

var TDSTM = require('../../config/App.js');

TDSTM.createDirective('modalRender', ['$log', function ($log) {
    $log.debug('ModalWindowActivation loaded');
    return {
        restrict: 'EA',
        link: function() {
            $('.modal-dialog').draggable({
                handle: ".modal-header"
            });
        }
    };
}]);